class Experience {
  final int id;
  final String title;
  final String startYear;
  final String endYear;
  final String city;
  final String country;
  final String company;

  Experience({
    required this.id,
    required this.title,
    required this.startYear,
    required this.endYear,
    required this.city,
    required this.country,
    required this.company,
  });
}
